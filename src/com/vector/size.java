package com.vector;

public class size {

	// Variabeln
	private int Width;
	private int Height;
	
	public size()
	{
		
	}
	
	public size(int Width, int Height)
	{
		this.Width = Width;
		this.Height = Height;
	}
	
	public void setWidth(int Width)
	{
		this.Width = Width;
	}
	
	public void setHeight(int Height)
	{
		this.Height = Height;
	}
	
	public int getWidth()
	{
		return this.Width;
	}
	
	public int getHeight()
	{
		return this.Height;
	}
	
	
}
