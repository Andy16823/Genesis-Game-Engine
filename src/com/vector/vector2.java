package com.vector;

public class vector2 {

	// Variables
	private int X;
	private int Y;
	
	public vector2()
	{

	}
	
	public vector2(int X, int Y)
	{
		this.X = X;
		this.Y = Y;
	}
	
	public void setX(int X)
	{
		this.X = X;
	}
	
	public void setY(int Y)
	{
		this.Y = Y;
	}
	
	public int getX()
	{
		return this.X;
	}
	
	public int getY()
	{
		return this.Y;
	}
	
	
	
}
